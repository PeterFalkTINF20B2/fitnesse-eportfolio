package codeToTest;

public class Calculator {
	public double sum(double op1, double op2) {
		return op1 + op2;
	}

	public double dif(double op1, double op2) {
		return op1 - op2;
	}

	public double mul(double op1, double op2) {
		return op1 * op2;
	}

	public double div(double op1, double op2) {
		return op1 / op2;
	}
}